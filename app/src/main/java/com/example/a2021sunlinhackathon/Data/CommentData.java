package com.example.a2021sunlinhackathon.Data;

public class CommentData {
    public String commentUserProfile;
    public String commentUsername;
    public String commentComment;

    public CommentData() {

    }

    public String getCommentUserProfile() {
        return commentUserProfile;
    }

    public void setCommentUserProfile(String commentUserProfile) {
        this.commentUserProfile = commentUserProfile;
    }

    public String getCommentUsername() {
        return commentUsername;
    }

    public void setCommentUsername(String commentUsername) {
        this.commentUsername = commentUsername;
    }

    public String getCommentComment() {
        return commentComment;
    }

    public void setCommentComment(String commentComment) {
        this.commentComment = commentComment;
    }
}
